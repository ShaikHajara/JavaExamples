package com.java.examples1;

public class Item {

	private String Name;
	private int Quantity;

	public String getName() {
		return Name;
	}

	public int getQuantity() {
		return Quantity;
	}

	public void setName(String Name) {
		this.Name = Name;
	}

	public void setQuantity(int Quantity) {
		this.Quantity = Quantity;
	}
}
